const BADGE_MAP: Record<string, keyof NavBadgeCounts> = {
  "/leads": "newLeads",
  "/tickets": "openTickets",
};

export function Sidebar({ isAdmin, badgeCounts, className }: SidebarProps) {
  const pathname = usePathname();
  const { collapsed, toggle } = useSidebar();
  const { t } = useLanguage();

  function isActive(href: string, exact?: boolean) {
    if (exact) return pathname === href;
    if (href === "/dashboard") return pathname === "/dashboard";
    return pathname.startsWith(href);
  }

  return (
    <aside
      className={cn(
        "flex-col bg-[#2A5959] font-[Helvetica,Arial,sans-serif] transition-all duration-300 ease-in-out",
        collapsed ? "w-[68px]" : "w-64",
        className
      )}
    >
      {/* Logo */}
      <div className="flex items-center gap-2 px-3 py-4">
        {!collapsed && (
          <div className="flex flex-1 items-center gap-2">
            <svg viewBox="0 0 32 32" fill="none" className="size-6 text-white">
              <circle cx="16" cy="16" r="14" stroke="currentColor" strokeWidth="2" />
              <ellipse cx="16" cy="16" rx="8" ry="14" stroke="currentColor" strokeWidth="2" />
              <path d="M2 16h28" stroke="currentColor" strokeWidth="2" />
              <path d="M4 8h24" stroke="currentColor" strokeWidth="1.5" />
              <path d="M4 24h24" stroke="currentColor" strokeWidth="1.5" />
            </svg>
            <span className="text-[14px] font-bold leading-[1.5] text-white">
              Votre Site Pro
            </span>
          </div>
        )}
        {collapsed && (
          <button
            onClick={toggle}
            className="mx-auto flex size-10 items-center justify-center rounded-lg text-white/70 hover:text-white"
            title="Expand sidebar"
          >
            <svg className="size-5 rotate-180" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 19.5 8.25 12l7.5-7.5" />
            </svg>
          </button>
        )}
      </div>

      {/* Nav links */}
      <nav className="flex flex-1 flex-col gap-1 border-b border-[#DBDBDB]/30 px-3 pb-6">
        {dashboardItems.map((item) => {
          const label = t(item.labelKey);
          const badgeKey = BADGE_MAP[item.href];
          const badgeCount = badgeKey && badgeCounts ? badgeCounts[badgeKey] : 0;
          return (
            <Link
              key={item.href}
              href={item.href}
              title={collapsed ? label : undefined}
              className={cn(
                "flex items-center gap-3 rounded-lg px-2 py-1.5 transition-colors",
                collapsed && "justify-center px-0",
                isActive(item.href)
                  ? "bg-[#1A4040] text-white"
                  : "text-[#DBDBDB] hover:bg-[#1A4040]/50 hover:text-white"
              )}
            >
              <span className="relative">
                {item.icon}
                {collapsed && badgeCount > 0 && (
                  <span className="absolute -right-1.5 -top-1.5 flex h-4 min-w-4 items-center justify-center rounded-full bg-[#F2612E] px-1 text-[10px] font-bold text-white">
                    {badgeCount > 99 ? "99+" : badgeCount}
                  </span>
                )}
              </span>
              {!collapsed && (
                <>
                  <span className="text-[16px] leading-[1.5]">{label}</span>
                  {badgeCount > 0 && (
                    <span className="ml-auto flex h-5 min-w-5 items-center justify-center rounded-full bg-[#F2612E] px-1.5 text-xs font-bold text-white">
                      {badgeCount > 99 ? "99+" : badgeCount}
                    </span>
                  )}
                </>
              )}
            </Link>
          );
        })}

        {isAdmin && (
          <>
            <div className="my-3 border-t border-[#DBDBDB]/30" />
            {adminItems.map((item) => {
              const label = t(item.labelKey);
              return (
                <Link
                  key={item.href}
                  href={item.href}
                  title={collapsed ? label : undefined}
                  className={cn(
                    "flex items-center gap-3 rounded-lg px-2 py-1.5 transition-colors",
                    collapsed && "justify-center px-0",
                    isActive(item.href, item.exact)
                      ? "bg-[#1A4040] text-white"
                      : "text-[#DBDBDB] hover:bg-[#1A4040]/50 hover:text-white"
                  )}
                >
                  {item.icon}
                  {!collapsed && (
                    <span className="text-[16px] leading-[1.5]">{label}</span>
                  )}
                </Link>
              );
            })}
          </>
        )}
      </nav>

      {/* Spacer */}
      <div className="flex-1" />
    </aside>
  );
}
